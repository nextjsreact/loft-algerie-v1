// Fonction pour écrire le fichier JSON
function writeEnglishCommonFile() {
  const filePath = path.join(__dirname, '../public/locales/en/common.json');
  
  try {
    // Créer le répertoire s'il n'existe pas
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }
    
    // Écrire le fichier avec les traductions anglaises
    fs.writeFileSync(filePath, JSON.stringify(englishCommonTranslations, null, 2), 'utf8');
    
    console.log('✅ Successfully updated English common.json with correct translations');
    console.log(`📁 File location: ${filePath}`);
    
  } catch (error) {
    console.error('❌ Error writing English common.json:', error.message);
    process.exit(1);
  }
}

